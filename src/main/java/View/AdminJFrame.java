    private void loadComboBoxData() {
        // Tải dữ liệu KhuVuc
        ResultSet rsKhuVuc = KhuVucDAO.getAllKhuVuc();
        try {
            while (rsKhuVuc.next()) {
                cboKhuVuc.addItem(rsKhuVuc.getString("TenKhuVuc"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading KhuVuc data: " + e.getMessage());
        }

        // Tải dữ liệu TinhTrang
        ResultSet rsTinhTrang = TinhTrangDAO.getAllTinhTrang();
        try {
            while (rsTinhTrang.next()) {
                cboTinhTrang.addItem(rsTinhTrang.getString("TenTinhTrang"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading TinhTrang data: " + e.getMessage());
        }
    }

